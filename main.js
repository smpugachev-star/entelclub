  function openProductModal(id) {
    var p = PRODUCT_DETAILS[id];
    if (!p) return;
    var overlay = document.getElementById("pmodal");
    document.getElementById("pmodal-name").textContent = p.name;
    document.getElementById("pmodal-price").innerHTML = p.price === "По запросу"
      ? "Цена: <strong>по запросу</strong>"
      : "Цена: <strong>" + p.price + "</strong> <span style=\"font-size:11px;\">с НДС 22%</span>";

    var imgWrap = document.getElementById("pmodal-img-wrap");
    imgWrap.innerHTML = p.img
      ? '<img src="' + p.img + '" alt="' + p.name + '">'
      : '<span class="pmodal-noimg">Фото уточняется</span>';

    var specsEl = document.getElementById("pmodal-specs");
    if (p.specs && p.specs.length) {
      var html = "<ul>";
      p.specs.forEach(function (s) { html += "<li>" + s + "</li>"; });
      html += "</ul>";
      specsEl.innerHTML = html;
    } else {
      specsEl.innerHTML = '<div class="pmodal-empty"><strong>Подробные характеристики уточняются</strong>'
        + 'Совпадение с карточкой товара на entelradio.ru не найдено — заполните этот блок вручную в коде сайта '
        + '(объект PRODUCT_DETAILS, ключ «' + id + '», поле specs).</div>';
    }
    overlay.classList.add("active");
    document.body.style.overflow = "hidden";
  }

